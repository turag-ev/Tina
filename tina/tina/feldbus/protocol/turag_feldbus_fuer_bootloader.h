#define TURAG_FELDBUS_BOOTLOADER_ATMEGA_RESPONSE_FAIL_CONTENT		0xFD
///@}



/**
 * @name MCU-IDs 
 * @{
 */
#define TURAG_FELDBUS_BOOTLOADER_MCU_ID_INVALID				0x0000

#define TURAG_FELDBUS_BOOTLOADER_MCU_ID_ATMEGA8				0x0793
