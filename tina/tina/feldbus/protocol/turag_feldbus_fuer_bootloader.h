#define TURAG_FELDBUS_BOOTLOADER_RESPONSE_UNLOCK_REJECTED		0x01

///@}


/**
 * @name Atmega Commands
 * @{
 */
#define TURAG_FELDBUS_BOOTLOADER_ATMEGA_GET_PAGE_SIZE			0x11
#define TURAG_FELDBUS_BOOTLOADER_ATMEGA_GET_FUSES				0x12
